class FPNBlock(nn.Layer):
    def __init__(self, pyramid_channels, skip_channels):
        super().__init__()
        self.skip_conv = nn.Conv2D(skip_channels, pyramid_channels, kernel_size=1)

    def forward(self, x, skip=None):
        x = F.interpolate(x, scale_factor=2, mode="nearest")
        skip = self.skip_conv(skip)
        x = x + skip
        return x

class Conv3x3GNReLU(nn.Layer):
    def __init__(self, in_channels, out_channels, upsample=False):
        super().__init__()
        self.upsample = upsample
        self.block = nn.Sequential(
            nn.Conv2D(
                in_channels, out_channels, (3, 3), stride=1, padding=1, bias_attr=False
            ),
            nn.GroupNorm(32, out_channels),
            nn.ReLU(),
        )

    def forward(self, x):
        x = self.block(x)
        if self.upsample:
            x = F.interpolate(x, scale_factor=2, mode="bilinear", align_corners=True)
        return x

class SegmentationBlock(nn.Layer):
    def __init__(self, in_channels, out_channels, n_upsamples=0):
        super().__init__()

        blocks = [Conv3x3GNReLU(in_channels, out_channels, upsample=bool(n_upsamples))]

        if n_upsamples > 1:
            for _ in range(1, n_upsamples):
                blocks.append(Conv3x3GNReLU(out_channels, out_channels, upsample=True))

        self.block = nn.Sequential(*blocks)

    def forward(self, x):
        return self.block(x)

class MergeBlock(nn.Layer):
    def __init__(self, policy):
        super().__init__()
        if policy not in ["add", "cat"]:
            raise ValueError(
                "`merge_policy` must be one of: ['add', 'cat'], got {}".format(
                    policy
                )
            )
        self.policy = policy

    def forward(self, x):
        if self.policy == 'add':
            return sum(x)
        elif self.policy == 'cat':
            return paddle.concat(x, axis=1)
        else:
            raise ValueError(
                "`merge_policy` must be one of: ['add', 'cat'], got {}".format(self.policy)
            )


class FPNDecoder(nn.Layer):
    def __init__(
            self,
            encoder_channels,
            encoder_depth=5,
            pyramid_channels=256,
            segmentation_channels=128,
            dropout=0.1,
            merge_policy="add",
    ):
        super().__init__()

        self.out_channels = segmentation_channels if merge_policy == "add" else segmentation_channels * 4
        if encoder_depth < 3:
            raise ValueError("Encoder depth for FPN decoder cannot be less than 3, got {}.".format(encoder_depth))

        encoder_channels = encoder_channels[::-1]
        encoder_channels = encoder_channels[:encoder_depth + 1]

        self.p5 = nn.Conv2D(encoder_channels[0], pyramid_channels, kernel_size=1)
        self.p4 = FPNBlock(pyramid_channels, encoder_channels[1])
        self.p3 = FPNBlock(pyramid_channels, encoder_channels[2])
        self.p2 = FPNBlock(pyramid_channels, encoder_channels[3])

        self.seg_blocks = nn.LayerList([
            SegmentationBlock(pyramid_channels, segmentation_channels, n_upsamples=n_upsamples)
            for n_upsamples in [3, 2, 1, 0]
        ])

        self.merge = MergeBlock(merge_policy)
        self.dropout = nn.Dropout2D(p=dropout)

    def forward(self, *features):
        c2, c3, c4, c5 = features[-4:]

        p5 = self.p5(c5)
        p4 = self.p4(p5, c4)
        p3 = self.p3(p4, c3)
        p2 = self.p2(p3, c2)

        feature_pyramid = [seg_block(p) for seg_block, p in zip(self.seg_blocks, [p5, p4, p3, p2])]
        x = self.merge(feature_pyramid)
        x = self.dropout(x)

        return x


class swimb_fpn(nn.Layer):
    def __init__(self,
                 encoder='resnet34',
                 encoder_depth=4,
                 in_channels=3,
                 pretrained=True,
                 classes=2,
                 decoder_pyramid_channels: int = 256,
                 decoder_segmentation_channels: int = 128,
                 decoder_merge_policy: str = "add",
                 decoder_dropout: float = 0.2,
                 pretrain=None,
                 ):
        super(swimb_fpn, self).__init__()

        # ENCODER
        self.encoder = SwinTransformer(
        pretrain_img_size=384,
        embed_dim=128,
        depths=[2, 2, 18, 2],
        num_heads=[4, 8, 16, 32],
        window_size=12,
        pretrained=pretrain
        )
        encoder_channels = [128, 256, 512, 1024]


        # DECODER
        self.decoder = FPNDecoder(
            encoder_channels=encoder_channels,
            encoder_depth=encoder_depth,
            pyramid_channels=decoder_pyramid_channels,
            segmentation_channels=decoder_segmentation_channels,
            dropout=decoder_dropout,
            merge_policy=decoder_merge_policy,
        )

        # PREDICT
        self.pred_head = nn.Conv2D(decoder_segmentation_channels, classes, 3, 1, 1)

    def forward(self, x):
        encoder_feats = self.encoder(x)

        decoder_feat = self.decoder(*encoder_feats)
        out = self.pred_head(decoder_feat)
        out = F.interpolate(out, size=x.shape[-2:], mode='bilinear', align_corners=False)
        # out = self.up(out)
        # out = self.bn(self.conv1(out))
        # out = self.up2(out)
        # out = self.conv2(out)

        return out


class swimb_fpn_linear(nn.Layer):
    def __init__(self,
                 pretrain1=None,
                 pretrain2=None,
                 ):
        super(swimb_fpn_linear, self).__init__()

        # ENCODER
        self.encoder = swimb_fpn()
        if pretrain1 != None:
            param_dict = paddle.load(pretrain1)
            self.encoder.load_dict(param_dict)

        # DECODER
        self.encoder2 = SwinTransformer(
        pretrain_img_size=384,
        in_chans=1,
        embed_dim=128,
        depths=[2, 2, 18, 2],
        num_heads=[4, 8, 16, 32],
        window_size=12,
        pretrained=pretrain2,
        )


        self.avgpool = nn.AdaptiveAvgPool2D((1, 1))
        self.flatten = paddle.nn.Flatten()
        self.linear = paddle.nn.Linear(1024, 512)
        self.linear2 = paddle.nn.Linear(512, 8)


    def forward(self, x):
        seg_feats = self.encoder(x)
        out = paddle.argmax(seg_feats, axis=1).astype(paddle.float32).unsqueeze(1)
        out = self.encoder2(out)[-1]
        out = self.avgpool(out)

        out = self.flatten(out)
        out = self.linear(out)
        out = self.linear2(out)


        return seg_feats, out



